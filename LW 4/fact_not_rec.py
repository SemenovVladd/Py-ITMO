def fact_not_rec(n):
    """
    :param n: значение числа, факториал, которого хотим вычислить
    """
    c = 1
    #заводим счетчик
    for i in range(1, n + 1):
        c *= i
        #начинаем перемножать числа с 1, заканчиваем n (само n+1 не входит)
    return c
    #возвращаем значение факториала